int	ft_str_is_numeric(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] > 0 && str[i] < 48) || (str[i] > 58))
		{
			return (0);
		}
		i++ ;
	}
	return (1);
}
